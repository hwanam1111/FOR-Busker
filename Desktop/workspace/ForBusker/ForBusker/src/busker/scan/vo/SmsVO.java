package busker.scan.vo;

public class SmsVO {

	private int smsNO;
	private String smsSendEmail;
	private String smsContent;
	private String smsReceiveEmail;
	private String smsSendTime;
	private String smsStatus;
	
	public int getSmsNO() {
		return smsNO;
	}
	public void setSmsNO(int smsNO) {
		this.smsNO = smsNO;
	}
	public String getSmsSendEmail() {
		return smsSendEmail;
	}
	public void setSmsSendEmail(String smsSendEmail) {
		this.smsSendEmail = smsSendEmail;
	}
	public String getSmsContent() {
		return smsContent;
	}
	public void setSmsContent(String smsContent) {
		this.smsContent = smsContent;
	}
	public String getSmsReceiveEmail() {
		return smsReceiveEmail;
	}
	public void setSmsReceiveEmail(String smsReceiveEmail) {
		this.smsReceiveEmail = smsReceiveEmail;
	}
	public String getSmsSendTime() {
		return smsSendTime;
	}
	public void setSmsSendTime(String smsSendTime) {
		this.smsSendTime = smsSendTime;
	}
	public String getSmsStatus() {
		return smsStatus;
	}
	public void setSmsStatus(String smsStatus) {
		this.smsStatus = smsStatus;
	}
			
}
