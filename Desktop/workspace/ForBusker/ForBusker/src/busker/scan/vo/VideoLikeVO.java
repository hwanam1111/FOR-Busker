package busker.scan.vo;

public class VideoLikeVO {

	private int likeNo;
	private String memLoginEmail;
	private int videoNo;
	
	public int getLikeNo() {
		return likeNo;
	}
	public void setLikeNo(int likeNo) {
		this.likeNo = likeNo;
	}
	public String getMemLoginEmail() {
		return memLoginEmail;
	}
	public void setMemLoginEmail(String memLoginEmail) {
		this.memLoginEmail = memLoginEmail;
	}
	public int getVideoNo() {
		return videoNo;
	}
	public void setVideoNo(int videoNo) {
		this.videoNo = videoNo;
	}
	
	
}
