package busker.scan.vo;

public class BackedVO {
	

	private int backNo;
	private String memEmail;
	private String backName;
	private String backVideo;
	private String backContent;
	private String backNeeds;
	private String backTel;
	private String backDate;

	
	public int getBackNo() {
		return backNo;
	}
	public void setBackNo(int backNo) {
		this.backNo = backNo;
	}
	
	public String getMemEmail() {
		return memEmail;
	}
	public void setMemEmail(String memEmail) {
		this.memEmail = memEmail;
	}
	public String getBackName() {
		return backName;
	}
	public void setBackName(String backName) {
		this.backName = backName;
	}
	public String getBackVideo() {
		return backVideo;
	}
	public void setBackVideo(String backVideo) {
		this.backVideo = backVideo;
	}
	public String getBackContent() {
		return backContent;
	}
	public void setBackContent(String backContent) {
		this.backContent = backContent;
	}
	public String getBackNeeds() {
		return backNeeds;
	}
	public void setBackNeeds(String backNeeds) {
		this.backNeeds = backNeeds;
	}
	public String getBackTel() {
		return backTel;
	}
	public void setBackTel(String backTel) {
		this.backTel = backTel;
	}
	public String getBackDate() {
		return backDate;
	}
	public void setBackDate(String backDate) {
		this.backDate = backDate;
	}

}

 